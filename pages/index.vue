export default {
  name: 'IndexPage',
  mounted() {
    this.$setFillHeight()
    let viewWindowWidth = window.innerWidth
    window.addEventListener('resize', () => {
      if (viewWindowWidth === window.innerWidth) return
      viewWindowWidth = window.innerWidth
      this.$setFillHeight()
    })

    let color = this.randomIntFromInterval(1, 2)
    color === 1 ? color = 'hsla(190, 100%, 50%)' : color = 'hsla(340, 100%, 50%)'
    this.tagCanvas(color)
  },
  methods: {
    randomIntFromInterval(min, max) {
      return Math.floor(Math.random() * (max - min + 1) + min);
    },
    
    tagCanvas(color) {
      let options = {
          depth: 1.1,
          fadeIn: 3000,
          freezeActive: true,
          freezeDecel: true,
          initial: [0.3, -0.1],
          maxSpeed: 0.06,
          noSelect: true,
          outlineThickness : 0.5,
          shuffleTags: true,
          shape: 'sphere',
          textColour: color,
          textFont: 'HelveticaNeue-CondensedBold',
          zoom: 0.8,
          pinchZoom: true,
          wheelZoom: false,
      }
      TagCanvas.Start('myCanvas', '', options);
    },

  }
}
